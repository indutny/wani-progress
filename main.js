function easeInOutQuad(x) {
  return x < 0.5 ? 2 * x * x : 1 - Math.pow(-2 * x + 2, 2) / 2;
}

const ANIMATION_MS = 500;

function animated(elem) {
  let raf;

  let start;
  let prev = 0;
  let value = 0;

  function update() {
    const t = Math.min(Math.max(0, (Date.now() - start) / ANIMATION_MS), 1);

    const transient = easeInOutQuad(t) * (value - prev) + prev;
    elem.innerText = Math.round(transient);
    if (t < 1) {
      requestAnimationFrame(update);
    } else {
      prev = value;
    }
  }

  return (newValue) => {
    if (newValue === undefined) {
      elem.innerText = '?';
      return;
    }
    start = Date.now();
    value = newValue;
    update();
  };
}

function getStage(name) {
  const stage = document.querySelector(`.stage.${name}`);

  const total = stage.querySelector('.total');
  const radicals = stage.querySelector('.category.radicals .value');
  const kanji = stage.querySelector('.category.kanji .value');
  const vocabulary = stage.querySelector('.category.vocabulary .value');

  return {
    total: animated(total),
    radicals: animated(radicals),
    kanji: animated(kanji),
    vocabulary: animated(vocabulary),
  };
}

